-- ============================================================
-- LINE FAQs — per-device-line FAQs for SEO landing pages
--   (iPhone / iPad / Samsung).
-- Rendered both visually (FaqAccordion) and as FAQPage JSON-LD
-- on /repairs/iphone, /repairs/ipad, /repairs/samsung.
-- Editable via /dashboard/faqs.
-- ============================================================

CREATE TABLE line_faqs (
  id           UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  device_line  TEXT NOT NULL CHECK (device_line IN ('iphone','ipad','samsung')),
  question     TEXT NOT NULL,
  answer       TEXT NOT NULL,
  sort_order   INT  NOT NULL DEFAULT 0,
  is_published BOOLEAN NOT NULL DEFAULT true,
  created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX line_faqs_line_order_idx ON line_faqs (device_line, sort_order);

ALTER TABLE line_faqs ENABLE ROW LEVEL SECURITY;

CREATE POLICY "public_read" ON line_faqs
  FOR SELECT USING (is_published = true);

CREATE POLICY "admin_all" ON line_faqs
  FOR ALL USING (auth.role() = 'authenticated');

